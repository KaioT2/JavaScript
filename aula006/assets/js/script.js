//Como criar variáveis

//Pode-se usar: let - var - const

//const = é uma constante, ou seja, não há como modificar ela durante a execução do programa

/* Funciona 

    let nome = "Kaio"
    console.log(nome)

    nome = "João"
    console.log(nome)

================================
Funciona

    var nome = "Kaio"
    console.log(nome)

    nome = "João"
    console.log(nome)

================================ */

//Não funciona pois 'nome' foi criado como uma constante e nesse caso não pode ser modificado 

    const nome = "Kaio"
    console.log(nome)

